package com.week4_d3.string;

public class StringSwitchCase {
    public static void main(String[] args) {
        String fruits = "Apple";

        switch (fruits){
            case "Mango" :
                System.out.println("ing of fruits");
                break;

            case "Apple" :
                System.out.println("One apple a day");
                break;

            default :
                System.out.println("Not a fruit");
        }
    }
}
